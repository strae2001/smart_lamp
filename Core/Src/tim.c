/*	 TIM3计时用于计算台灯工作时长 */

uint32_t sit_time = 0;				// 台灯前有人的时间
uint8_t  timer_running = 0;			// 定时器是否正在运行 默认非运行态

void timer3_start(void)
{
	// 清除更新中断标志位 防止一开始计时就进中断
	__HAL_TIM_CLEAR_IT(&htim3, TIM_IT_UPDATE);
	
	// 启动计数器并使能更新中断
	HAL_TIM_Base_Start_IT(&htim3);
	timer_running = 1;						// 改变定时器状态为运行态
}

void timer3_stop(void)
{
	HAL_TIM_Base_Stop_IT(&htim3);
	timer_running = 0;
}

void timer3_toggle(void)
{
	if(timer_running)
	{
		timer3_stop();
	}
	else
		timer3_start();
}

uint32_t sit_time_get(void)
{
	return sit_time;
}

void sit_time_set(uint32_t val)
{
	sit_time = val;
}

